# Shows several ways to do escape sequences

# Using double backslash to escape \n
my_string = "This is a line with a literal \\n character."
print(my_string)  # Output: This is a line with a literal \n character.

# Using raw string to escape \n
my_string = r"This is a line with a literal \n character."
print(my_string)  # Output: This is a line with a literal \n character.